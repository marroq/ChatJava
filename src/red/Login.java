    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().equals("admin")) {            
            if (new String(jPasswordField1.getPassword()).equals("loginAdmin")) {
                value++;
                Servidor server = new Servidor();
                server.setVisible(true);
                jTextField1.setText(null);
                jPasswordField1.setText(null);
                jTextField1.requestFocus();
                
                if (value==2) {
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password incorrecto", "ERROR", JOptionPane.ERROR_MESSAGE);
                jTextField1.setText(null);
                jPasswordField1.setText(null);
                jTextField1.requestFocus();
            }
        } else if (jTextField1.getText().equals("client")) {
            if (new String(jPasswordField1.getPassword()).equals("loginClient")) {
                value++;
                Cliente client = new Cliente();
                client.setVisible(true);
                jTextField1.setText(null);
                jPasswordField1.setText(null);
                jTextField1.requestFocus();
                
                if (value==2) {
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password incorrecto", "ERROR", JOptionPane.ERROR_MESSAGE);
                jTextField1.setText(null);
                jPasswordField1.setText(null);
                jTextField1.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Usuario incorrecto", "ERROR", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText(null);
            jPasswordField1.setText(null);
            jTextField1.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

